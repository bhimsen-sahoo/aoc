# frozen_string_literal: true

#input
# 0,9 -> 5,9
# 8,0 -> 0,8
# 9,4 -> 3,4
# 2,2 -> 2,1
# 7,0 -> 7,4
# 6,4 -> 2,0
# 0,9 -> 2,9
# 3,4 -> 1,4
# 0,0 -> 8,8
# 5,5 -> 8,2

# output:
# [
#   "0,9 -> 5,9",
#   "8,0 -> 0,8",
#   "9,4 -> 3,4",
#   "2,2 -> 2,1",
#   "7,0 -> 7,4",
#   "6,4 -> 2,0",
#   "0,9 -> 2,9",
#   "3,4 -> 1,4",
#   "0,0 -> 8,8",
#   "5,5 -> 8,2"
# ]
def data
  File.open('aoc2021/day5/input.txt').read.split("\n")
end

# output:
# [
#   {:start=>{:x1=>"0", :y1=>"9"}, :end=>{:x2=>"5", :y2=>"9"}},
#   {:start=>{:x1=>"8", :y1=>"0"}, :end=>{:x2=>"0", :y2=>"8"}},
#   {:start=>{:x1=>"9", :y1=>"4"}, :end=>{:x2=>"3", :y2=>"4"}},
#   {:start=>{:x1=>"2", :y1=>"2"}, :end=>{:x2=>"2", :y2=>"1"}},
#   {:start=>{:x1=>"7", :y1=>"0"}, :end=>{:x2=>"7", :y2=>"4"}},
#   {:start=>{:x1=>"6", :y1=>"4"}, :end=>{:x2=>"2", :y2=>"0"}},
#   {:start=>{:x1=>"0", :y1=>"9"}, :end=>{:x2=>"2", :y2=>"9"}},
#   {:start=>{:x1=>"3", :y1=>"4"}, :end=>{:x2=>"1", :y2=>"4"}},
#   {:start=>{:x1=>"0", :y1=>"0"}, :end=>{:x2=>"8", :y2=>"8"}},
#   {:start=>{:x1=>"5", :y1=>"5"}, :end=>{:x2=>"8", :y2=>"2"}}
# ]
def parsed_data
  data.map do |l|
    s_c, e_c = l.split(' -> ')
    x1, y1 = s_c.split(',')
    x2, y2 = e_c.split(',')

    { start: { x1: x1, y1: y1 }, end: { x2: x2, y2: y2 } }
  end
end
